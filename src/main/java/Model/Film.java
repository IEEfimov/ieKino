package Model;

public class Film {
    public int id = 0;
    public String name = "";
    public String age = "";
    public int duration = 0;
    public int rating = 0;
    public String description = "";
    public int cost = 0;

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }

    public String getAge() {
        return age;
    }

    public int getDuration() {
        return duration;
    }

    public int getRating() {
        return rating;
    }

    public String getDescription() {
        return description;
    }

    public int getCost() {
        return cost;
    }
}
