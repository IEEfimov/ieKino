package Model;

public class Ticket {
    public int id = 0;
    public int film = 0;
    public String email = "";
    public int place = 0;
    public String filmname = "";

    public int getId() {
        return id;
    }

    public int getFilm() {
        return film;
    }

    public String getEmail() {
        return email;
    }

    public int getPlace() {
        return place;
    }

    public String getFilmname() {
        return filmname;
    }
}
